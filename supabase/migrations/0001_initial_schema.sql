-- -----------------------------------------------------------------------------
-- USERS
-- Public profile table — extends Supabase's internal auth.users table.
-- One row per user, auto-created by trigger in 0003_triggers.sql on signup.
-- -----------------------------------------------------------------------------
create table public.users (
  id                    uuid references auth.users on delete cascade primary key,
  username              text unique not null,
  bio                   text,
  avatar_url            text,
  stripe_account_id     text,
  -- Tracks Stripe Connect onboarding state for the creator payout flow
  stripe_account_status text default 'not_connected'
    check (stripe_account_status in ('not_connected', 'pending', 'active', 'restricted')),
  follower_count        int default 0 not null,
  following_count       int default 0 not null,
  is_admin              boolean default false not null,
  created_at            timestamptz default now() not null
);

comment on table public.users is
