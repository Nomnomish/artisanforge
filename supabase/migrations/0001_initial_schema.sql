-- WORK FILES
-- Individual files attached to a work (STL, OBJ, GLB, images).
-- A work can have multiple files — e.g. an STL + its converted GLB + a preview image.
-- -----------------------------------------------------------------------------
create table public.work_files (
  id                uuid primary key default gen_random_uuid(),
  work_id           uuid references public.works on delete cascade not null,
  file_type         text not null
    check (file_type in ('stl', 'obj', 'glb', 'png', 'jpg', 'jpeg', 'webp')),
  storage_path      text not null,   -- Path within Supabase Storage bucket
  public_url        text,            -- Set for files in works-public bucket
  thumbnail_url     text,            -- 800×800 WEBP, generated by Edge Function
  printify_image_id text,            -- Set after design is pushed to Printify (Phase 5)
  is_download_file  boolean default false not null, -- True = this is the purchasable file
  file_size_bytes   bigint,
  created_at        timestamptz default now() not null
);

comment on table public.work_files is
