-- -----------------------------------------------------------------------------
-- WORKS
-- A creator's uploaded piece — 3D model, painting, illustration, or t-shirt design.
-- A work is a portfolio item. It becomes sellable when a product row is created for it.
-- -----------------------------------------------------------------------------
create table public.works (
  id             uuid primary key default gen_random_uuid(),
  user_id        uuid references public.users on delete cascade not null,
  title          text not null,
  description    text,
  category       text not null
    check (category in ('3d-models', 'painted-minis', 't-shirt-designs', 'traditional-art', 'digital-art')),
  tags           text[] default '{}',
  is_published   boolean default false not null,
  like_count     int default 0 not null,
  comment_count  int default 0 not null,
  -- Full-text search vector — automatically kept in sync by Postgres.
  -- Searched via the GIN index below. Never write to this column directly.
  search_vector  tsvector generated always as (
                   to_tsvector('english',
                     coalesce(title, '') || ' ' || coalesce(description, '')
                   )
                 ) stored,
  created_at     timestamptz default now() not null,
  updated_at     timestamptz default now() not null
);

comment on table public.works is
  'A creator upload — portfolio item and/or basis for a product listing.';
comment on column public.works.search_vector is
  'Auto-generated tsvector for full-text search. Searched via works_search_idx GIN index.';
comment on column public.works.tags is
  'Free-form tag array. Searched via works_tags_idx GIN index.';

-- Full-text search index
create index works_search_idx on public.works using gin(search_vector);
-- Tag filter index
create index works_tags_idx on public.works using gin(tags);
-- Feed queries: creator's works, ordered by date
create index works_user_id_idx on public.works(user_id);
-- Feed queries: ordered by creation date (New tab)
create index works_created_at_idx on public.works(created_at desc);
-- Feed queries: filter published works efficiently
create index works_is_published_idx on public.works(is_published);


