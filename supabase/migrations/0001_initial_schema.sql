-- -----------------------------------------------------------------------------
-- PRODUCTS
-- A work listed for sale. One work can have multiple product types.
-- product_type drives the fulfillment path on payment.
-- -----------------------------------------------------------------------------
create table public.products (
  id                    uuid primary key default gen_random_uuid(),
  work_id               uuid references public.works on delete cascade not null,
  user_id               uuid references public.users on delete cascade not null,
  product_type          text not null
    check (product_type in ('digital_download', 'printify_pod', 'physical_contact')),
  price_cents           int check (price_cents > 0),     -- Null for physical_contact (price on request)
  -- Printify fields — populated during Phase 5 POD setup
  printify_product_id   text,
  printify_blueprint_id text,
  enabled_variants      jsonb,   -- [{variantId, price_cents, colour, size}]
  mockup_urls           text[] default '{}',
  is_active             boolean default true not null,
  created_at            timestamptz default now() not null
);

comment on table public.products is
  'A sellable product backed by a work. product_type determines the fulfillment path.';
comment on column public.products.product_type is
  'digital_download: file delivery via signed URL | printify_pod: Printify fulfillment | physical_contact: Gen2 / contact creator.';
comment on column public.products.enabled_variants is
  'For printify_pod only. Array of {variantId, price_cents, colour, size} objects.';

create index products_work_id_idx on public.products(work_id);
create index products_user_id_idx on public.products(user_id);
create index products_is_active_idx on public.products(is_active);


